func GetRequestTrace(c *gin.Context) {
	trace, found, err := model.GetRequestTraceByRequestID(c.Param("request_id"))
	if err != nil {
		common.ApiError(c, err)
		return
	}
	if !found {
		common.ApiError(c, errors.New("request trace not found"))
		return
	}
	common.ApiSuccess(c, trace)
}

